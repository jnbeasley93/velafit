// ─────────────────────────────────────────────────────────────
// Helper: get exercises filtered by user profile
// ─────────────────────────────────────────────────────────────

/**
 * Map the user-facing equipment labels from the fitness profile
 * to the internal equipment tags used in the exercise library.
 */
const EQUIPMENT_MAP = {
  'None — bodyweight only': ['none'],
  'Resistance bands': ['bands'],
  'Dumbbells and/or Kettlebells': ['dumbbells', 'kettlebells'],
  'Pull-up bar': ['pull-up-bar'],
  'Full home gym': ['none', 'bands', 'dumbbells', 'kettlebells', 'pull-up-bar'],
  'Commercial gym access': ['none', 'bands', 'dumbbells', 'kettlebells', 'pull-up-bar', 'barbell', 'gym'],
};

/**
 * Returns `count` random exercises matching the user's equipment and
 * intensity level, optionally filtered by category.
 *
 * @param {Object}  fitnessProfile  - The user's fitness_profile JSON from Supabase
 * @param {string}  [category]      - Optional category filter (push, pull, legs, core, cardio, shoulders, arms)
 * @param {number}  [count=5]       - How many exercises to return
 * @param {number}  [intensityLevel=2] - The user's current intensity_level (1-5)
 * @returns {Array} A randomly-selected subset of matching exercises
 */
export function getExercisesForProfile(fitnessProfile, category, count = 5, intensityLevel = 2) {
  // Build the set of equipment tags this user has access to
  const userEquipment = new Set(['none']); // everyone gets bodyweight
  const profileEquipment = fitnessProfile?.equipment || [];
  const equipmentList = Array.isArray(profileEquipment) ? profileEquipment : [profileEquipment];

  for (const label of equipmentList) {
    const mapped = EQUIPMENT_MAP[label];
    if (mapped) {
      for (const tag of mapped) {
        userEquipment.add(tag);
      }
    }
  }

  // Filter exercises
  const pool = exercises.filter((ex) => {
    // Equipment check: the exercise must use only equipment the user has
    const hasEquipment = ex.equipment.every((tag) => userEquipment.has(tag));
    if (!hasEquipment) return false;

    // Difficulty check: exercise difficulty should be at most intensityLevel + 1
    // so users are challenged but not overwhelmed
    if (ex.difficulty > intensityLevel + 1) return false;

    // Category filter
    if (category && ex.category !== category) return false;

    return true;
  });

  // Shuffle and return `count` exercises
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
